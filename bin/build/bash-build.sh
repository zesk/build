__bashRunCommandsHead() {
  printf "%s\n" "#!/usr/bin/env bash" "# Automatically generated by ${BASH_SOURCE[0]}"
}
__bashRunCommandsScript() {
  local tools="$1" && shift
  printf "%s\n" "" "# shellcheck source=/dev/null" "source \"$tools\"" "bashPrompt" "$@"
}

# Argument: handler - Function. Error handler
# Argument: rcFile - File. Required. File to modify.
# Argument: tools - File. Required. Main tools.sh script for Zesk Build.
# Argument: ... - Arguments. Optionals. Additional lines (Bash code) to write to `rcFile`.
__bashProfileConfiguration() {
  local handler="$1" && shift
  local rcFile="$1" && shift
  if [ ! -f "$rcFile" ]; then
    catchEnvironment "$handler" __bashRunCommandsHead >>"$rcFile" || return $?
    catchEnvironment "$handler" chmod 0700 "$rcFile" || return $?
  elif grep -q 'bashPrompt' "$rcFile"; then
    return 0
  else
    catchEnvironment "$handler" __bashRunCommandsScript "$@" >>"$rcFile" || return $?
  fi
}

# fn: {base}
# Install base packages required before running a command, and set up a user profile to load environment upon login.
# Argument: --rc-extras textLine ... -- - String. Optional. Set of commands to be written to the first `.bashrc` file.
# Argument: command ... - Callable. Optional. Arguments to pass to `bash`
# DOC TEMPLATE: --help 1
# Argument: --help - Flag. Optional. Display this help.
__bashBuild() {
  local handler="_${FUNCNAME[0]}"

  local extraCommands=()
  if [ "${1-}" = "--rc-extras" ]; then
    shift
    while [ $# -gt 0 ]; do
      if [ "$1" == "--" ]; then
        shift && break
      fi
      extraCommands+=("$1") && shift
    done
  fi

  local tools="${BASH_SOURCE[0]%/*}/tools.sh"
  # shellcheck source=/dev/null
  if ! source "$tools"; then
    printf -- "%s\n" "Unable to source $tools"
    exec bash "$@"
  fi
  [ "${1-}" != "--help" ] || __help "$handler" "$@" || return 0

  local home
  if home=$(userHome); then
    rcFile="$home/.bashrc"
    tools="$(realPath "$tools")" || return $?
    __bashProfileConfiguration "$handler" "$rcFile" "$tools" "${extraCommands[@]+"${extraCommands[@]}"}" || return $?
  fi
  local start
  start=$(timingStart)
  statusMessage decorate success "Setting up operating system with required base packages ..."
  catchReturn "$handler" packageUpdate || return $?
  catchReturn "$handler" packageInstall || return $?
  statusMessage --last timingReport "$start" "System set up in"
  exec bash "$@"
}
___bashBuild() {
  # __IDENTICAL__ usageDocument 1
  usageDocument "${BASH_SOURCE[0]}" "${FUNCNAME[0]#_}" "$@"
}

__bashBuild "$@"
